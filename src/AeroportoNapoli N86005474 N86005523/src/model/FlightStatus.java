package model;

/**
 * The enum Flight status.
 */
public enum FlightStatus {
    /**
     * Scheduled flight status.
     */
    SCHEDULED,
    /**
     * Boarding flight status.
     */
    BOARDING,
    /**
     * Delayed flight status.
     */
    DELAYED,
    /**
     * Departed flight status.
     */
    DEPARTED,
    /**
     * In air flight status.
     */
    IN_AIR,
    /**
     * Landed flight status.
     */
    LANDED,
    /**
     * Cancelled flight status.
     */
    CANCELLED,
    /**
     * Diverted flight status.
     */
    DIVERTED,
    /**
     * Unknown flight status.
     */
    UNKNOWN
}


